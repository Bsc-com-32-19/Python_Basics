for item in 'python':
    print(item)

# list of items
for item in ['mimie', 'olivia','shira']:
    print(item)

# list of numbers
for item in [1, 2, 3, 4, 5]:
    print(item)

# range of numbers using range function
for item in range(10):
     print(item)

# start from 5 to 10
for item in range(5,10):
     print(item)

# jump two number then print the 3rd number
for item in range(5, 10, 2):
     print(item)
